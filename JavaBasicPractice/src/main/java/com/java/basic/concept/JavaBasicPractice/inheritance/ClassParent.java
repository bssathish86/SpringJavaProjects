package com.java.basic.concept.JavaBasicPractice.inheritance;

public class ClassParent {

	public ClassParent() {
		super();
		System.out.println("Constructor ClassParent");
	}

	public void m1() {
		System.out.println("ClassParent -> m1 ()");
	}

	public static void m3() {
		System.out.println("ClassParent -> m3 ()");
	}
}
