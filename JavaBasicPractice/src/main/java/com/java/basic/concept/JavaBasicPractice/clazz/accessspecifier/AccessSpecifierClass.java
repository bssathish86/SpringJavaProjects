package com.java.basic.concept.JavaBasicPractice.clazz.accessspecifier;

public class AccessSpecifierClass {

	// java access specifier are four types..
	// public, private, protected, default.
	public static void main(String[] args) {

		System.out.println("java access specifier are four types.");
		System.out.println("....................................");
		System.out.println("public:- Visible to the world,");
		System.out.println("private:- Visible to the class only,");
		System.out.println("protected:- Visible to the package and all subclasses,");
		System.out.println("default:- Visible to the package.");
		System.out.println("....................................");
		System.out.println("Java there is no terminology for specifier, everything is by default \n"
				+ "considered as modifier only. So public, private, protected, default, \n"
				+ "final, abstract, static, strictfp, synchronized, native, transient \n"
				+ "and volatile are all modifiers only.");
		System.out.println("....................................");
	}
}
